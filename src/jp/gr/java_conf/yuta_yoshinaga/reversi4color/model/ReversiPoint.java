package jp.gr.java_conf.yuta_yoshinaga.reversi4color.model;

import java.io.Serializable;

////////////////////////////////////////////////////////////////////////////////
///	@class		ReversiPoint
///	@brief		リバーシポイントクラス
///
////////////////////////////////////////////////////////////////////////////////
public class ReversiPoint implements Serializable
{
	private int x;												//!< X座標
	private int y;												//!< Y座標

	////////////////////////////////////////////////////////////////////////////////
	///	@brief			ゲッター
	///	@fn				int getX()
	///	@return			int x
	///	@author			Yuta Yoshinaga
	///	@date			2018.04.01
	///
	////////////////////////////////////////////////////////////////////////////////
	public int getX() {
		return x;
	}

	////////////////////////////////////////////////////////////////////////////////
	///	@brief			セッター
	///	@fn				void setX(int x)
	///	@param[in]		int x
	///	@return			ありません
	///	@author			Yuta Yoshinaga
	///	@date			2018.04.01
	///
	////////////////////////////////////////////////////////////////////////////////
	public void setX(int x) {
		this.x = x;
	}

	////////////////////////////////////////////////////////////////////////////////
	///	@brief			ゲッター
	///	@fn				int getY()
	///	@return			int y
	///	@author			Yuta Yoshinaga
	///	@date			2018.04.01
	///
	////////////////////////////////////////////////////////////////////////////////
	public int getY() {
		return y;
	}

	////////////////////////////////////////////////////////////////////////////////
	///	@brief			セッター
	///	@fn				void setY(int y)
	///	@param[in]		int y
	///	@return			ありません
	///	@author			Yuta Yoshinaga
	///	@date			2018.04.01
	///
	////////////////////////////////////////////////////////////////////////////////
	public void setY(int y) {
		this.y = y;
	}

	////////////////////////////////////////////////////////////////////////////////
	///	@brief			コンストラクタ
	///	@fn				public ReversiPoint()
	///	@return			ありません
	///	@author			Yuta Yoshinaga
	///	@date			2018.04.01
	///
	////////////////////////////////////////////////////////////////////////////////
	public ReversiPoint(){
		this.x = 0;
		this.y = 0;
	}
}

